package com.simplilearn.pojo;

public class EProduct {
	private Long eproductId;
	private String eproductName;
	private Long eproductPrice;

	public Long getEproductId() {
		return eproductId;
	}

	public void setEproductId(Long eproductId) {
		this.eproductId = eproductId;
	}

	public String getEproductName() {
		return eproductName;
	}

	public void setEproductName(String eproductName) {
		this.eproductName = eproductName;
	}

	public Long getEproductPrice() {
		return eproductPrice;
	}

	public void setEproductPrice(Long eproductPrice) {
		this.eproductPrice = eproductPrice;
	}
}